/// Specifies the quadrant alignment of a rectangle relative to a widget.
enum QuadrantAlignment {
  /// Aligns the rectangle above the widget.
  top,

  /// Aligns the rectangle to the left of the widget.
  left,

  /// Aligns the rectangle to the right of the widget.
  right,

  /// Aligns the rectangle below the widget.
  bottom,

  /// A special case where the rectangle covers the widget.
  inside;
}
